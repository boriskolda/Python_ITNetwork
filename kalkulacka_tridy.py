class Kalkulacka():
    """
    Definuje kalkulaÄku
    """
    
    cislo_1 = None
    cislo_2 = None

    def soucet(self):
        return self.cislo_1 + self.cislo_2

    def rozdil(self):
        return self.cislo_1 - self.cislo_2
    
    def soucin(self):
        return self.cislo_1 * self.cislo_2
    
    def podil(self):
        return self.cislo_1 / self.cislo_2
    
    
